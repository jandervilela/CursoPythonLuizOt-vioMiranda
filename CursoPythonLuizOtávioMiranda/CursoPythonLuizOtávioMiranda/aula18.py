"""
Operadores de comparação (relacionais)
OP      Significado         Exemplo (True)
>       maior               2 > 1
>=      maior ou igual      2 >= 2
<       menor               1 < 2
<=      menor ou igual      2 <= 2
==      igual               'a' == 'a'
!=      diferente           'a' != 'b'
"""
maior = 2 > 1
print(maior) # Eu Adcionei esse!
maior_ou_igual = 2 >= 2
print(maior_ou_igual) # Eu Adcionei esse!
menor = 1 < 2
print(menor) # Eu Adcionei esse!
menor_ou_igual = 2 <= 2
print(menor_ou_igual) # Eu Adcionei esse!
igual = 'a' == 'a'
print(igual) # Eu Adcionei esse!
diferente = 'a' != 'b'
